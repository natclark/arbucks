        <h2>About ArbiDAO</h2>

        <p>ArbiDAO was planned to be the first DAO (decentralized autonomous organization) native to an Ethereum Layer 2 network and the first DAO native to Arbitrum.</p>

        <p>The primary objective of this DAO would have been to create excellent user-facing open-source products on Arbiturm, including price charts and token analytics.</p>

        <p>Participation in this DAO would have been powered and incentivized by the BUCK token, a token with a fixed supply of 100,000,000 (100M).</p>

        <p>8% of the BUCK supply had been allocated to the ArbiDAO treasury.</p>

        <p>After closely listening to the community and discussing whether the DAO would really be necessary at this stage, that 8% of the supply has since been [burned](https://arbiscan.io/tx/0x4881aac3c7849259d89746358f5a69374a047661ea619fdf84cfa485b341e450) (sent to a dead wallet).</p>

